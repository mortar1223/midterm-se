class Buyer {
	String name;
	int age;

	public Buyer(String name, int age){
		this.name = name;
		this.age = age;
	}

	public String getName(){
		return name;
	}

	public void setName(String name){
		this.name = name;
	}

	public Int getAge(){
		return age;
	}

	public void setAge(int age){
		this.age = age;
	}
}